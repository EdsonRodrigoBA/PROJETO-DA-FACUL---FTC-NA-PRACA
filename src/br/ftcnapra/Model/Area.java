package br.ftcnapra.Model;

public class Area {
	
	private Integer areaCodigo;
	private String areaNome;
	private String areaDescricao;
	
	public Integer getAreaCodigo() {
		return areaCodigo;
	}
	public void setAreaCodigo(Integer areaCodigo) {
		this.areaCodigo = areaCodigo;
	}
	public String getAreaNome() {
		return areaNome;
	}
	public void setAreaNome(String areaNome) {
		this.areaNome = areaNome;
	}
	public String getAreaDescricao() {
		return areaDescricao;
	}
	public void setAreaDescricao(String areaDescricao) {
		this.areaDescricao = areaDescricao;
	}
	

	
	
	
	
}
